// Function implementations
inline void drawZetTechSplash(TFT_eSPI& tft, int16_t x, int16_t y, 
                              uint16_t color_fg, uint16_t color_bg) {
    // Clear screen with background color
    tft.fillScreen(color_bg);
    
    // Draw the bitmap
    drawBitmap(tft, epd_bitmap_ztech, x, y, SPLASH_WIDTH, SPLASH_HEIGHT, color_fg, color_bg);
}

inline void showSplashScreen(TFT_eSPI& tft, uint16_t delay_ms) {
    // Display the splash screen
    drawZetTechSplash(tft, 0, 0, TFT_WHITE, TFT_BLACK);
    
    // Wait for specified duration
    delay(delay_ms);
}

inline void drawBitmap(TFT_eSPI& tft, const unsigned char* bitmap, int16_t x, int16_t y,
                       int16_t w, int16_t h, uint16_t color, uint16_t bg) {
    int16_t byteWidth = (w + 7) / 8; // Bitmap scanline pad = whole byte
    uint8_t byte = 0;

    for (int16_t j = 0; j < h; j++, y++) {
        for (int16_t i = 0; i < w; i++) {
            if (i & 7) {
                byte <<= 1;
            } else {
                byte = pgm_read_byte(&bitmap[j * byteWidth + i / 8]);
            }
            
            if (byte & 0x80) {
                tft.drawPixel(x + i, y, color);
            } else {
                tft.drawPixel(x + i, y, bg);
            }
        }
    }
}

